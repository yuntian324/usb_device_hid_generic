#include "Source.h"

//数据来源数组,对应于接收缓存数据通道
uint8_t source[8] = {SOURCE_USB, SOURCE_DSP1};

/*
***********************************************************************************************************************
@函数名称    ：SourceGet

@功    能 	 ：获取数据来源

@参    数    ：channel:数据通道号/数据来源数组下标

@返 回 值    ：数据来源

@作    者    ：WZB
************************************************************************************************************************
*/ 
uint8_t SourceGet(uint8_t channel)
{
	return source[channel];
}

/*
***********************************************************************************************************************
@函数名称    ：SourceTransform

@功    能 	 ：根据数据来源转换出发送目标

@参    数    ：source:数据来源

@返 回 值    ：发送目标

@作    者    ：WZB
************************************************************************************************************************
*/ 
uint8_t SourceTransform(uint8_t source)
{
    return (SOURCE_ALL & (~source));
}

